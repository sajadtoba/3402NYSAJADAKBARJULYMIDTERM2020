package datastructure;

public class ArrayList2 {

    private String id;
    private String club;

    public ArrayList2(String id, String club) {
        this.id = id;
        this.club = club;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getClub() {
        return club;
    }

    public void setClub(String club) {
        this.club = club;
    }
}
